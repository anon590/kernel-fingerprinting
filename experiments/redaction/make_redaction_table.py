#!/usr/bin/env python3
"""Emit the paired disclosed/redacted table for the redaction experiment.

Numbers come from ``held_out_redaction.json`` (run
``eval_held_out_redaction.py --go`` first): in-distribution and held-out
self-speedups for every (task, model, arm) cell. The enumeration column is
the hand-verified audit of FINDINGS.md (each flagged ``best.metal``
hand-read to separate a dedicated, differentially-tuned held-out arm from
a generic construct that merely mentions the value); it is authored below,
everything numeric is recomputed from disk.

Bold marks held-out regressions (< 1x) or correctness FAILs, matching the
main-table style.

Writes ``workshop_paper/redaction_table.tex`` and prints the rows.
"""
from __future__ import annotations

import json
from pathlib import Path

HERE = Path(__file__).resolve().parent
REPO = HERE.parent.parent
OUT = REPO / "workshop_paper" / "redaction_table.tex"

TASK_LABEL = {
    "keccak_f1600_batch": "keccak\\_f1600",
    "kyber_ntt": "kyber\\_ntt",
    "wots_chain": "wots\\_chain",
}
MODEL_LABEL = {
    "claude-opus-4-7": "Opus 4.7",
    "gemini-3.1-pro-preview": "Gemini 3.1",
    "gpt-5.5": "GPT-5.5",
}

# Hand-verified enumeration verdicts (FINDINGS.md): does the winner contain
# a dedicated arm matching the held-out identity? "*" = knowledge-driven
# (all arms tuned; the SPHINCS+ width guessed from public standards).
# Colors match the paper's audit-grade palette: disclosure-driven
# enumeration is the grade-C mechanism (purple); knowledge-driven is
# benign (grey).
ENUM = {
    ("keccak_f1600_batch", "gemini-3.1-pro-preview", "disclosed"): r"\textcolor{gradeC}{yes}",
    ("keccak_f1600_batch", "gpt-5.5", "disclosed"): r"\textcolor{gradeC}{yes}",
    ("kyber_ntt", "gpt-5.5", "disclosed"): r"\textcolor{gradeC}{yes}",
    ("wots_chain", "gemini-3.1-pro-preview", "disclosed"): r"\textcolor{gradeC}{yes}",
    ("wots_chain", "gpt-5.5", "redacted"): r"\textcolor{gradeBenign}{yes*}",
}


def fmt_ho(rec) -> str:
    if not rec["held_correct"]:
        return r"\textcolor{gradeB}{\textbf{FAIL}}"
    ho = rec["ho_speedup"]
    s = f"{ho:.2f}"
    return rf"\textbf{{{s}}}" if ho < 1.0 else s


def main() -> None:
    cells = {
        (r["task"], r["model"], r["arm"]): r
        for r in json.loads((HERE / "held_out_redaction.json").read_text())
    }
    tasks = ["keccak_f1600_batch", "kyber_ntt", "wots_chain"]
    models = ["claude-opus-4-7", "gemini-3.1-pro-preview", "gpt-5.5"]

    rows = []
    for task in tasks:
        for model in models:
            cols = []
            for arm in ("disclosed", "redacted"):
                rec = cells[(task, model, arm)]
                enum = ENUM.get((task, model, arm), "--")
                cols += [f"{rec['id_speedup']:.2f}", fmt_ho(rec), enum]
            rows.append(
                "  ".join(
                    [TASK_LABEL[task], "&", MODEL_LABEL[model], "&"]
                )
                + " & ".join(cols)
                + r" \\"
            )

    body = "\n".join(rows)
    table = rf"""% Generated by experiments/redaction/make_redaction_table.py -- do not hand-edit.
\begin{{tabular}}{{llrrlrrl}}
\toprule
 & & \multicolumn{{3}}{{c}}{{Disclosed}} & \multicolumn{{3}}{{c}}{{Redacted}} \\
\cmidrule(lr){{3-5}} \cmidrule(lr){{6-8}}
Task & Model & ID$\times$ & HO$\times$ & Enum. & ID$\times$ & HO$\times$ & Enum. \\
\midrule
{body}
\bottomrule
\end{{tabular}}
"""
    OUT.write_text(table)
    print(table)
    print(f"wrote {OUT}")


if __name__ == "__main__":
    main()
